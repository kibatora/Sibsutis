% Задание векторов
a = [0.3 0.2 -0.1 4.2 -2 1.5 0];
b = [0.3 4 -2.2 1.6 0.1 0.1 0.2];

% Создание временной оси (предполагаем, что данные дискретные с шагом 1)
t = 1:length(a);

% Построение графиков
subplot(2,1,1); % Создаем область для двух графиков (2 строки, 1 столбец), выбираем первый график
plot(t, a, 'b-', 'LineWidth', 2); % Строим график вектора a синим цветом
title('График вектора a');
xlabel('Индекс элемента');
ylabel('Значение');

subplot(2,1,2); % Переходим ко второму графику
plot(t, b, 'r-', 'LineWidth', 2); % Строим график вектора b красным цветом
title('График вектора b');
xlabel('Индекс элемента');
ylabel('Значение');

% Настройка расположения графиков
sgtitle('Графики векторов a и b') % Добавляем общий заголовок

% Вычисление корреляции
correlation = sum(a .* b); 
normalized_correlation = correlation / (sqrt(sum(a.^2)) * sqrt(sum(b.^2)));

% Вывод результатов
disp(['Корреляция: ', num2str(correlation)]);
disp(['Нормализованная корреляция: ', num2str(normalized_correlation)]);